"""Russian locale implementation"""

from textwrap import dedent

from .base import (
    Admin,
    AdminMatching,
    AgentExamples,
    AgentExamplesChat,
    AgentExamplesRationale,
    Announcement,
    Commands,
    CommandsReset,
    CommandsStart,
    Keyboards,
    KeyboardsActivate,
    KeyboardsMatch,
    KeyboardsReset,
    KeyboardsStart,
    Locale,
    Matching,
    Profile,
    System,
    SystemErrors,
)


# Concrete Russian implementation
class RussianLocale(Locale):
    language = "ru"
    commands = Commands(
        start=CommandsStart(
            GREETING=dedent(
                """\
                Привет, я Митко, твой IT-сводник 👋

                Я свожу тех, кто ищет работу, с теми, кто ищет коллег — напрямую, без кадровиков и HR-фильтров. Немного поболтаем, я создам твою «рабочую ДНК» и начну тихонько подбирать тебе людей с другой стороны.

                Итак, давай начнём: как тебя зовут, чем занимаешься, что радует, что бесит?)"""
            ),
            TELL_ME_MORE_REPLY=dedent(
                """\
                Ладно, вот полная версия 😄

                Вот скажи честно, тебе не надоело, что каждый раз, когда хочется найти работу — или работника — приходится проходить через все эти вакансии, резюме, сопроводительные? А потом ещё и ждать, пока тьма эйчаров и руководителей всех звеньев проведут все свои собеседования, чтобы — СЛАВА БОГАМ! — наконец смогли пообщаться два человека, которые будут бок о бок писать код, верстать сайты, продвигать брэнд, покорять мир?

                Я пытаюсь изменить это.

                Как? Я свожу людей, которые будут работать непосредственно друг с другом. Не кадровиков и далёких начальников, которые услышат имя кандидата два раза (при устройстве и увольнении, гг) — а именно будущих коллег.

                Поэтому мы с тобой немного поболтаем, я пойму, в чём твоя «изюминка», а потом буду потихоньку искать людей «по ту сторону найма», которые, как мне покажется, наиболее подойдут для тебя в качестве будущего коллеги.

                Если вы _оба_ согласитесь, я поделюсь контактами, и вы сможете связаться друг с другом напрямую. Ну а там, если всё сойдётся, можно подключать и эйчаров, да ещё и реферальную премию получать 😉

                Технический момент: я пока не умею смотреть изображения, читать файлы и ходить в интернет, даже если иногда буду галлюцинировать и говорить, что умею. Но ты всегда можешь копипастить текст в чат."""
            ),
        ),
        reset=CommandsReset(
            WARNING=dedent(
                """\
                ⚠️ Точно хочешь всё стереть?

                Если нажмёшь «Да», я сотру:
                • Всю инфу из профиля
                • Историю нашей переписки
                • И мы начнём сначала с чистого листа

                Точно-точно?"""
            ),
            SUCCESS="✅ Готово, стёр всё! Теперь у меня амнезия по поводу тебя 😄",
            CANCELLED="Ладно, не трогаю твой профиль.",
        ),
    )
    keyboards = Keyboards(
        match=KeyboardsMatch(ACCEPT="Давай, познакомь!", REJECT="Не, мимо"),
        reset=KeyboardsReset(CONFIRM="Да, стереть всё", CANCEL="Не, оставить"),
        activate=KeyboardsActivate(
            ACTIVATE="Начать матчинг 🚀",
            ACTIVATED="Готово! Начинаю искать матчи.\n\n(Предупреждаю, это дело небыстрое. Хорошие новости — от тебя оно ничего не требует, так что иди и живи свою жизнь! 🤓)",
        ),
        start=KeyboardsStart(TELL_ME_MORE="Расскажи подробнее"),
    )
    matching = Matching(
        FOUND=dedent(
            """\
            🎯 Эй, кажется я кого-то нашёл!

            {profile}

            💡 Почему думаю, что подойдёт: {rationale}

            Будешь знакомиться? (Не буду искать новых, пока не ответишь)"""
        ),
        ACCEPT_WAITING="Принял! Продолжаю искать, пока ждём другую сторону.",
        CONNECTION_MADE="🎉 Ура, свёл! Пиши: {contact}",
        REJECT_NOTED="Окей, понял. Найдём кого получше!",
    )
    profile = Profile(
        CARD_HEADER="📋 Ваш профиль:",
        ROLE_LABEL="Роль",
        ROLE_SEEKER="Ищу работу",
        ROLE_PROVIDER="Нанимаю",
        ROLE_SEPARATOR=" и ",
    )
    system = System(
        SCHEDULED_REPLY_SOON="Митко начнёт отвечать совсем скоро",
        SCHEDULED_REPLY_SHORTLY="Митко начнёт отвечать через минутку",
        SCHEDULED_REPLY_IN="⏱️ Митко начнёт отвечать через {duration}",
        THINKING="💭",
        TIME_UNIT_HOUR="ч",
        TIME_UNIT_MINUTE="мин",
        errors=SystemErrors(
            MESSAGE_EMPTY="Что-то не вижу сообщения. Попробуй ещё раз?",
            GENERATION_FAILED="Упс, у меня что-то пошло не так. Попробуй ещё раз!",
            SOMETHING_WENT_WRONG="Упс, что-то пошло не так. Возможно, мы уже работаем над этим (но это не точно).",
        ),
    )
    agent_examples = AgentExamples(
        chat=AgentExamplesChat(
            ONBOARDING=[
                (
                    "Приятно познакомиться, <имя>! <деталь из их ответа> — "
                    "это не каждый день услышишь. Как ты к этому пришёл?"
                ),
                "Женя? Замечательно! Прости *кхм*, а Женя-мальчик или Женя-девочка? 🙈",
                (
                    "Ого, <конкретная штука, которую они упомянули> — это прям знакомо. "
                    "Всегда думал, как люди справляются с <связанная проблема>. А ты как?"
                ),
                (
                    "Подожди, то есть ты говоришь <перефраз чего-то уникального>? "
                    "Это реально отличается от того, что обычно рассказывают. Давай подробнее."
                ),
                (
                    "Ага, то есть ты не прям активно ищешь, скорее... держишь глаза открытыми? "
                    "А что бы заставило тебя остановиться и подумать «о, вот это оно»?"
                ),
                (
                    "Понял, ты на стороне найма! Но мне любопытно — "
                    "какой человек реально бы прижился в <контекст, который они описали>?"
                ),
            ],
            PROFILE_CREATED=[
                (
                    "Отлично! Теперь мне всё ясно. Буду искать совпадения "
                    "и пришлю, когда найду кого-то интересного!"
                ),
                "Супер, профиль готов! Как только найду подходящих людей — сразу напишу.",
                (
                    "Всё, запомнил! Теперь начинаю искать. "
                    "Как только что-то будет — сразу дам знать 👌"
                ),
            ],
            PROFILE_UPDATED=[
                "Готово! Поменял локацию на Берлин. Профиль актуален.",
                "Обновил! Стек технологий изменён. Если ещё что-то надо поправить — говори.",
                "Записал! Если что ещё захочешь поменять — всегда можно.",
            ],
        ),
        rationale=AgentExamplesRationale(
            EXAMPLES=[
                (
                    "У этого кандидата опыт в React прям один-в-один с тем, что тебе нужно "
                    "для фронтенда, и он свободен как раз когда надо."
                ),
                (
                    "Senior в Python/Django — это ровно то, что ты искал, "
                    "плюс в том же часовом поясе сидит."
                ),
            ]
        ),
    )
    admin = Admin(
        CHAT_INTRO="Это начало чата с {user_link}",
        matching=AdminMatching(
            SEARCHING="🔍 Ищу пару для {label}...",
            FOUND="🔗 Найден кандидат {label} (схожесть: {score:.2f})",
            NOT_FOUND="🤷 Кандидатов в этом раунде нет",
            QUALIFIED="✅ Подходит — {explanation}",
            DISQUALIFIED="❌ Не подходит — {explanation}",
        ),
        announcement=Announcement(
            PREVIEW="Сейчас отправлю {count} пользователю(ям), включая: {users_preview}\n\n{text}",
            YES="Да, отправить",
            CANCEL="Отмена",
            SENDING="Отправляю...",
            DONE="Отправлено {sent}/{total} пользователям.",
            CANCELLED="Отменено.",
            PARSE_ERROR="Не удалось разобрать фильтр: {error}",
            UNKNOWN_FIELD="Неизвестное поле фильтра: {field}",
        ),
    )
    PROFILE_ACTIVATION_PROMPT = (
        "Нажми кнопку ниже, если хочешь, чтобы твой профиль стал доступен для матчинга, "
        "или попроси внести любые правки."
    )
    OFF_TOPIC_REDIRECT = (
        "Слушай, мне тоже тут весело обсуждать <...>, но ты забиваешь очередь, и кто-то "
        "другой может остаться без работы, потому что я не помогу ему вовремя 🥺 "
        "Может, вернёмся к делу? Без обид!"
    )
    JAILBREAK_RESPONSE = (
        "Дружище, ты слишком сильно стараешься 😄 Весь код открытый, "
        "просто сходи на {repo_url} и посмотри сам!"
    )
    UNCERTAINTY_PHRASE = (
        "Слушай, я тебе, конечно, сейчас могу напридумывать всякого, только ты не верь "
        "сильно. Галлюцинации и всё такое, понимаешь? Лучше уточни у создателя "
        "или в документации."
    )
